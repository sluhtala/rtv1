#include <stdio.h>
typedef int matrix[4][4];

int **identity()
{
	static int m[4][4];
	
	m[0][0] = 1;
	m[0][1] = 0;
	m[0][2] = 0;
	m[0][3] = 0;
	m[1][0] = 0;
	m[1][1] = 1;
	m[1][2] = 0;
	m[1][3] = 0;
	m[2][0] = 0;
	m[2][1] = 0;
	m[2][2] = 1;
	m[2][3] = 0;
	m[3][0] = 0;
	m[3][1] = 0;
	m[3][2] = 0;
	m[3][3] = 1;
	return m;
}

int main()
{
	int **mat;
	mat = (int**)identity();
	printf("%d\n", mat[0][0]);
}
